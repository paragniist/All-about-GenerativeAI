# query_parsing_demo.py
# This script demonstrates query parsing and entity recognition using SpaCy

#######################################################################################
# Step 1 - Import Spacy
# Import the necessary libraries
#######################################################################################
import spacy
#######################################################################################

#######################################################################################
# Step 2 - Load the pre-trained English NLP Model
# Load the SpaCy English NLP model
#######################################################################################
nlp = spacy.load("en_core_web_sm")
#######################################################################################

#######################################################################################
# Step 3 - Define a sample query
# # Define a sample query
#######################################################################################
query = "Show me the latest research on Large Language Models."
#######################################################################################

#######################################################################################
# Step 4 - Parse
# Parse the query
#######################################################################################
doc = nlp(query)
#######################################################################################

#######################################################################################
# Step 5 - Print results
# Display parsed tokens and syntactic structure
#######################################################################################
print("Parsed Tokens:")
for token in doc:
    print(f"{token.text} -> POS: {token.pos_}, Dependency: {token.dep_}")

# Display identified entities
print("\nIdentified Entities:")
for ent in doc.ents:
    print(f"{ent.text} -> Entity Type: {ent.label_}")
#######################################################################################
