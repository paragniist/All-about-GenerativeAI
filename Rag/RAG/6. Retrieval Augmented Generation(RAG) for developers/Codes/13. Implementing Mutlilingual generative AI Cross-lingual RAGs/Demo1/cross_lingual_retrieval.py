################################################################################################################
# Step 1 - Import Required Libraries
################################################################################################################
from transformers import MarianMTModel, MarianTokenizer
from sentence_transformers import SentenceTransformer, util
################################################################################################################

################################################################################################################
# Step 2 - Load the Translation Model and Tokenizer for English to Spanish
################################################################################################################
model_name = 'Helsinki-NLP/opus-mt-en-es'
translation_model = MarianMTModel.from_pretrained(model_name)
translation_tokenizer = MarianTokenizer.from_pretrained(model_name)
################################################################################################################

################################################################################################################
# Step 3 - Define the Translation Function that translates text from English to Spanish
################################################################################################################
def translate(text, model, tokenizer):
    inputs = tokenizer(text, return_tensors="pt", padding=True)
    translated = model.generate(**inputs)
    return [tokenizer.decode(t, skip_special_tokens=True) for t in translated]
################################################################################################################

################################################################################################################
# Step 4 - Translate the Source Text in English.  We hard code "Hello, how are you?" 
################################################################################################################
source_text = "Hello, how are you?"
translated_text = translate(source_text, translation_model, translation_tokenizer)
print("Translated Text in Spanish:", translated_text)
################################################################################################################

################################################################################################################
# Step 5 - Load the Pre-Trained Sentence Embedding Model  
################################################################################################################
embedding_model = SentenceTransformer('stsb-mpnet-base-v2')
################################################################################################################

################################################################################################################
# Step 6 - Encode the Translated Text into Embeddings 
################################################################################################################
translated_embedding = embedding_model.encode(translated_text, convert_to_tensor=True)
################################################################################################################

################################################################################################################
# Step 7 - Define the list of possible responses in Spanish
################################################################################################################
# "Hola, ¿cómo estás?" - "Hello, how are you?"
# "Estoy bien, gracias." - "I am fine, thank you."
# "¿Qué haces?" - "What are you doing?"
################################################################################################################
responses = ["Hola, ¿cómo estás?", "Estoy bien, gracias.", "¿Qué haces?"]
response_embeddings = embedding_model.encode(responses, convert_to_tensor=True)
################################################################################################################

################################################################################################################
# Step 8 - Compute the Cosine Similarity Between the Translated Text and the Responses 
################################################################################################################
similarities = util.pytorch_cos_sim(translated_embedding, response_embeddings)
################################################################################################################

################################################################################################################
# Step 9 - Find and Print the best matching response
################################################################################################################
best_match_idx = similarities.argmax()
print("Best Response:", responses[best_match_idx])
################################################################################################################